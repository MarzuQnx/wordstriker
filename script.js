let lang = localStorage.getItem('wordstriker_lang') || 'id'

function applyLanguage(l) {
  lang = l
  localStorage.setItem('wordstriker_lang', l)
  document.documentElement.lang = l
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n
    const text = i18n[l]?.[key]
    if (text !== undefined) el.innerHTML = text
  })
  const btn = document.getElementById('langToggle')
  if (btn) btn.textContent = l === 'id' ? 'EN' : 'ID'
  const desc = document.querySelector('meta[name="description"]')
  if (desc) {
    desc.setAttribute('content',
      l === 'id'
        ? 'Word Striker — Game puzzle kata Nusantara. Susun kata, temukan senjata, kalahkan bos!'
        : 'Word Striker — Nusantara word puzzle game. Arrange letters, discover weapons, defeat the boss!')
  }
}

document.addEventListener('DOMContentLoaded', () => {
  applyLanguage(lang)

  document.getElementById('langToggle')?.addEventListener('click', () => {
    applyLanguage(lang === 'id' ? 'en' : 'id')
  })

  // --- Scroll navbar ---
  const navbar = document.querySelector('.navbar')
  let ticking = false
  window.addEventListener('scroll', () => {
    if (!ticking) {
      requestAnimationFrame(() => {
        navbar.style.borderBottomColor = window.scrollY > 40
          ? 'rgba(255,255,255,0.1)' : 'rgba(255,255,255,0.04)'
        ticking = false
      })
      ticking = true
    }
  })

  // --- Toast ---
  function showToast(msg) {
    const el = document.getElementById('toast')
    el.textContent = msg
    el.classList.remove('hidden')
    setTimeout(() => el.classList.add('hidden'), 3000)
  }

  // --- Smooth scroll for nav links ---
  document.querySelectorAll('.nav-links a').forEach(a => {
    a.addEventListener('click', e => {
      e.preventDefault()
      const id = a.getAttribute('href').slice(1)
      const target = document.getElementById(id)
      if (target) {
        target.scrollIntoView({ behavior: 'smooth', block: 'start' })
        history.replaceState(null, '', '#' + id)
      }
    })
  })

  // --- Google Play badge placeholder ---
  document.querySelector('.badge.google-play')?.addEventListener('click', e => {
    e.preventDefault()
    showToast(lang === 'id'
      ? '🔗 Tautan Google Play akan tersedia setelah aplikasi dipublikasikan.'
      : '🔗 Google Play link will be available once the app is published.')
  })

  // --- Intersection Observer for reveal ---
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.style.opacity = '1'
        entry.target.style.transform = 'translateY(0)'
      }
    })
  }, { threshold: 0.15 })

  document.querySelectorAll('.section, .about-card, .feature-item, .screenshot-img').forEach(el => {
    el.style.opacity = '0'
    el.style.transform = 'translateY(24px)'
    el.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out'
    observer.observe(el)
  })

  // --- Theme color ---
  const meta = document.querySelector('meta[name="theme-color"]')
  if (meta) {
    const darkBg = getComputedStyle(document.documentElement).getPropertyValue('--bg').trim()
    meta.setAttribute('content', darkBg || '#0D0D1A')
  }

  // --- Pixel Particles ---
  const canvas = document.getElementById('pixelParticles')
  if (canvas) {
    const ctx = canvas.getContext('2d')
    function resizeCanvas() {
      canvas.width = window.innerWidth
      canvas.height = window.innerHeight
    }
    resizeCanvas()
    window.addEventListener('resize', resizeCanvas)
    const particles = Array.from({ length: 60 }, () => ({
      x: Math.random() * canvas.width,
      y: Math.random() * canvas.height,
      size: Math.random() * 3 + 2,
      speed: Math.random() * 0.4 + 0.2,
      alpha: Math.random() * 0.5 + 0.2,
    }))
    function animateParticles() {
      ctx.clearRect(0, 0, canvas.width, canvas.height)
      particles.forEach(p => {
        p.y -= p.speed
        if (p.y < -10) { p.y = canvas.height + 10; p.x = Math.random() * canvas.width }
        ctx.fillStyle = `rgba(255,215,0,${p.alpha})`
        ctx.fillRect(Math.round(p.x), Math.round(p.y), p.size, p.size)
      })
      requestAnimationFrame(animateParticles)
    }
    animateParticles()
  }

  // --- Hero Parallax ---
  const layers = document.querySelectorAll('.layer')
  if (layers.length) {
    window.addEventListener('mousemove', e => {
      const x = (e.clientX / window.innerWidth - 0.5)
      const y = (e.clientY / window.innerHeight - 0.5)
      layers.forEach((layer, index) => {
        const depth = (index + 1) * 10
        layer.style.transform = `translate(${x * depth}px, ${y * depth}px)`
      })
    })
  }

  // --- Legendary Popup Demo ---
  setTimeout(() => {
    const popup = document.getElementById('legendaryPopup')
    if (popup) {
      popup.classList.add('show')
      setTimeout(() => popup.classList.remove('show'), 3500)
    }
  }, 2000)

  // --- Phone Carousel ---
  const phones = document.querySelectorAll('.phone')
  if (phones.length) {
    let active = 0
    setInterval(() => {
      phones.forEach(p => p.classList.remove('active'))
      active++
      if (active >= phones.length) active = 0
      phones[active].classList.add('active')
    }, 3000)
  }

  // --- Floating Weapon Discovery ---
  const weaponNames = ['KERIS', 'KUJANG', 'RENCONG', 'RAJA', 'GATOTKACA', 'MERIAM']
  const weaponEl = document.querySelector('.weapon-discovery')
  if (weaponEl) {
    let weaponIndex = 0
    setInterval(() => {
      weaponEl.textContent = weaponNames[weaponIndex]
      weaponIndex++
      if (weaponIndex >= weaponNames.length) weaponIndex = 0
    }, 8000)
  }

  // --- Combo Blast ---
  function comboBlast(text) {
    const el = document.createElement('div')
    el.className = 'combo-hit'
    el.textContent = text
    document.body.appendChild(el)
    setTimeout(() => el.remove(), 800)
  }

  // --- Combo on features section ---
  const featuresSection = document.getElementById('features')
  if (featuresSection) {
    const comboObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          comboBlast('CHAIN COMBO')
          comboObserver.unobserve(entry.target)
        }
      })
    }, { threshold: 0.3 })
    comboObserver.observe(featuresSection)
  }

  // --- Live Feed ---
  const feedContainer = document.getElementById('liveFeed')
  function showFeed(msg) {
    if (!feedContainer) return
    const el = document.createElement('div')
    el.className = 'feed-item'
    el.textContent = msg
    feedContainer.appendChild(el)
    setTimeout(() => { if (el.parentNode) el.remove() }, 4000)
  }
  const discoveries = [
    '⚔️ KERIS', '⚡ RAJA', '👑 MAJAPAHIT',
    '☠️ APOCALYPSE', '🔥 LEGEND', '🗡️ KUJANG',
    '💀 NECROMANCER', '🌀 MYSTIC COMBO'
  ]
  setInterval(() => {
    showFeed(discoveries[Math.floor(Math.random() * discoveries.length)])
  }, 6000)

  // --- BGM ---
  const bgm = document.getElementById('battleBgm')
  const bgmBtn = document.getElementById('bgmToggle')
  let bgmStarted = false
  async function startBgm() {
    if (bgmStarted || !bgm) return
    try {
      bgm.volume = 0.35
      await bgm.play()
      bgmStarted = true
    } catch (_) {}
  }
  document.addEventListener('click', startBgm, { once: true })
  bgmBtn?.addEventListener('click', async () => {
    if (bgm.paused) {
      await bgm.play()
      bgmBtn.textContent = '🔊 BGM ON'
    } else {
      bgm.pause()
      bgmBtn.textContent = '🔇 BGM OFF'
    }
  })

  // ============================================================
  // FIX 1: Victory Overlay — Close Button
  // ============================================================
  document.getElementById('victoryClose')?.addEventListener('click', () => {
    document.getElementById('victoryOverlay')?.classList.remove('show')
  })

  // ============================================================
  // FIX 2: Sticky Boss HP Bar
  // ============================================================
  const stickyBoss   = document.getElementById('stickyBoss')
  const stickyHpFill = document.getElementById('stickyHpFill')
  const stickyHpText = document.getElementById('stickyHpText')

  // Show sticky bar only after user scrolls past the hero section
  const heroSection = document.querySelector('.hero')
  if (heroSection && stickyBoss) {
    const heroObserver = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (!entry.isIntersecting) {
          stickyBoss.classList.remove('hidden')
        } else {
          stickyBoss.classList.add('hidden')
        }
      })
    }, { threshold: 0.1 })
    heroObserver.observe(heroSection)
  }

  // --- Scroll Boss HP + Dialog + Rage ---
  const bossHpFill = document.getElementById('bossHpFill')
  const bossHpText = document.getElementById('bossHpText')
  const bossDialog = document.getElementById('bossDialog')

  const dialogs = [
    { at: 0,   text: '"Kau tak akan lolos dari Ancol..."' },
    { at: 0.2, text: '"Aku mulai tertarik..."' },
    { at: 0.4, text: '"Kekuatanmu meningkat..."' },
    { at: 0.6, text: '"Mustahil..."' },
    { at: 0.8, text: '"Tidak... ini belum berakhir..."' },
  ]

  let lastDialogIdx  = -1
  let rageTriggered  = false
  let rageTextEl     = null

  function triggerRage() {
    if (rageTriggered) return
    rageTriggered = true

    setTimeout(() => {
      bossHpFill?.classList.add('rage')
      bossHpFill && (bossHpFill.style.width = '50%')
      stickyHpFill?.classList.add('rage')
      stickyHpFill && (stickyHpFill.style.width = '50%')
      comboBlast('RAGE MODE')
    }, 500)

    setTimeout(() => {
      bossHpFill?.classList.remove('rage')
      bossHpFill?.classList.add('rage-red')
      bossHpFill && (bossHpFill.style.width = '15%')
      stickyHpFill?.classList.remove('rage')
      stickyHpFill?.classList.add('rage-red')
      stickyHpFill && (stickyHpFill.style.width = '15%')
    }, 3000)

    setTimeout(() => {
      bossHpFill?.classList.remove('rage-red')
      bossHpFill?.classList.add('rage-dead')
      bossHpFill && (bossHpFill.style.width = '3%')
      stickyHpFill?.classList.remove('rage-red')
      stickyHpFill?.classList.add('rage-dead')
      stickyHpFill && (stickyHpFill.style.width = '3%')
    }, 5500)

    rageTextEl = document.createElement('div')
    rageTextEl.className = 'boss-rage-text'
    rageTextEl.textContent = '⚡ SI MANIS RAGE MODE ⚡'
    const bossUi = document.querySelector('.boss-ui')
    if (bossUi) bossUi.appendChild(rageTextEl)

    document.body.classList.add('shake')
    setTimeout(() => document.body.classList.remove('shake'), 300)
  }

  window.addEventListener('scroll', () => {
    const max      = document.documentElement.scrollHeight - window.innerHeight
    const progress = Math.min(window.scrollY / max, 1)

    // HP drain — main boss UI + sticky bar
    if (!rageTriggered) {
      const hp       = Math.max(0, 1000000 - Math.floor(1000000 * progress))
      const widthPct = (1 - progress) * 100 + '%'
      const hpLabel  = hp.toLocaleString() + ' HP'

      if (bossHpFill) bossHpFill.style.width = widthPct
      if (bossHpText) bossHpText.textContent  = hpLabel
      if (stickyHpFill) stickyHpFill.style.width = widthPct
      if (stickyHpText) stickyHpText.textContent  = hpLabel
    }

    // Dialog
    for (let i = dialogs.length - 1; i >= 0; i--) {
      if (progress >= dialogs[i].at) {
        if (lastDialogIdx !== i && bossDialog) {
          lastDialogIdx = i
          bossDialog.textContent = dialogs[i].text
          bossDialog.classList.add('show')
          setTimeout(() => bossDialog.classList.remove('show'), 4000)
        }
        break
      }
    }

    // Rage
    if (progress >= 0.7 && !rageTriggered) triggerRage()

    // Victory
    const victoryEl = document.getElementById('victoryOverlay')
    if (victoryEl && progress > 0.98) victoryEl.classList.add('show')
  })

  // --- Secret Combo Keyboard ---
  let typed = ''
  window.addEventListener('keydown', e => {
    typed += e.key.toUpperCase()
    if (typed.length > 10) typed = typed.slice(-10)
    const combos = ['RAJA', 'KERIS', 'GATOTKACA', 'KERAJAAN', 'SEMAR']
    for (const combo of combos) {
      if (typed.includes(combo)) {
        comboBlast('⚡ ' + combo + ' COMBO')
        typed = ''
        break
      }
    }
  })

  // --- Pixel Fog ---
  const fogCanvas = document.getElementById('pixelFog')
  if (fogCanvas) {
    const fctx = fogCanvas.getContext('2d')
    function resizeFog() {
      fogCanvas.width  = window.innerWidth
      fogCanvas.height = window.innerHeight
    }
    resizeFog()
    window.addEventListener('resize', resizeFog)
    const fogPixels = Array.from({ length: 120 }, () => ({
      x:      Math.random() * fogCanvas.width,
      y:      Math.random() * fogCanvas.height,
      size:   Math.random() * 6 + 2,
      speedX: (Math.random() - 0.5) * 0.3,
      speedY: (Math.random() - 0.5) * 0.15,
      alpha:  Math.random() * 0.03 + 0.01,
      color:  ['rgba(255,0,255,', 'rgba(0,212,255,', 'rgba(255,255,255,'][Math.floor(Math.random() * 3)]
    }))
    function animateFog() {
      fctx.clearRect(0, 0, fogCanvas.width, fogCanvas.height)
      fogPixels.forEach(p => {
        p.x += p.speedX
        p.y += p.speedY
        if (p.x < -10) p.x = fogCanvas.width  + 10
        if (p.x > fogCanvas.width  + 10) p.x = -10
        if (p.y < -10) p.y = fogCanvas.height + 10
        if (p.y > fogCanvas.height + 10) p.y = -10
        fctx.fillStyle = p.color + p.alpha + ')'
        fctx.fillRect(Math.round(p.x), Math.round(p.y), p.size, p.size)
      })
      requestAnimationFrame(animateFog)
    }
    animateFog()
  }

  // --- CTA clicks ---
  document.querySelector('.badge.boss-cta')?.addEventListener('click', e => {
    e.preventDefault()
    showToast(lang === 'id'
      ? '🔗 Akan tersedia setelah aplikasi dipublikasikan.'
      : '🔗 Available once the app is published.')
  })

  document.getElementById('victoryCta')?.addEventListener('click', e => {
    e.preventDefault()
    showToast(lang === 'id'
      ? '🔗 Akan tersedia setelah aplikasi dipublikasikan.'
      : '🔗 Available once the app is published.')
  })
})
